#for loop with list
l=[3,5,4,8,9]
for i in l:         #l is list print each element separately
    print(i)

#for loop with tuple
t=(3,5,4,8,9)
for i in t:         #t is tuple print each element separately
    print(i)

#for loop with string
s="Abhi"
